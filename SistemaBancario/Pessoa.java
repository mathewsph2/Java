
public class Pessoa
{
    protected String nome;
    private String cpf;
    private int idade;
    private char sexo;
    private double salario; 
    
    public void setNome(String nome){
        this.nome = nome; 
    }
    public String getNome(){
        return nome; 
    }
    
    public void setCpf(String cpf){
        this.nome = nome; 
    }
    public String getCpf(){
        return cpf; 
    }
    public void setIdade(int idade){
        this.idade = idade; 
    }
    public int getIdade(){
        return idade; 
    }
    
    public void setSexo(char nome){
        this.sexo = sexo; 
    }
    public char getSexo(){
        return sexo; 
    }
    
    public void setSalario(double nome){
        this.salario = salario; 
    }
    public double getSalario(){
        return salario; 
    }
    
    
}